use citas1;
CREATE TABLE IF NOT EXISTS PACIENTE (
             ID INTEGER AUTO_INCREMENT,
             HISTORIA VARCHAR (20),
             NOMBRE VARCHAR (500),
             CIF VARCHAR (20),
             PRIMARY KEY (ID)
)ENGINE INNODB;

CREATE TABLE IF NOT EXISTS CENTRO (
             ID INTEGER AUTO_INCREMENT,
             K_CENTRO VARCHAR (20),
             NOMBRE VARCHAR (500),
             CIF VARCHAR (20),
             PRIMARY KEY (ID)
)ENGINE INNODB;

CREATE TABLE IF NOT EXISTS CITAS (
             ID INTEGER AUTO_INCREMENT,
             HISTORIA VARCHAR (20),
             MEDICO VARCHAR (20),
             NOMBRE_MEDICO VARCHAR (500),
             DPTO VARCHAR (120),
             FAMTIPOCITA VARCHAR (120),
             TIPOCITA VARCHAR (120),
             FECHA VARCHAR (120),
             HORA VARCHAR (120),
             ESTADO VARCHAR (20),
             CEN INTEGER,
             CENTRODESC VARCHAR(500),
             PRIMARY KEY (ID)
)ENGINE INNODB;


